public class Range {
	public static void main (String args[]) {
		double testNumber = 3.0;
		if(testNumber > 7) {
			System.out.print("Greater than seven");
		}
		else if (testNumber < 5){
			System.out.print("Less than five");
		}
		else {
			System.out.print("Greater than five and Less than seven");
		}
	}

}
